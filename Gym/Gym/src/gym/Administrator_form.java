    private void open_class_open_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_open_class_open_ButtonActionPerformed
        // TODO add your handling code here:
        
         if (open_class_id_TextField.getText().toString().equals("") || open_class_des_TextArea.getText().toString().equals("")) 
        {
            
            JOptionPane.showMessageDialog(this, "Fill all data firist");
            
        } else {
             
            //Start get data from TextFieldes and Comboboxes.
            int class_id = Integer.parseInt(open_class_id_TextField.getText().toString());
            int trainer_id = Integer.parseInt(open_class_trainer_id_ComboBox.getSelectedItem().toString());
            String day = open_class_day_ComboBox.getSelectedItem().toString();
            int time = Integer.parseInt(open_class_time_ComboBox.getSelectedItem().toString());
            String type = open_class_type_ComboBox.getSelectedItem().toString();
            String discription = open_class_des_TextArea.getText().toString();
            //Start get data from TextFieldes and Comboboxes.
            
            //Start check if trainer has class in chossen day and time.
            boolean flage = false;
            int position = 0;
            for (int i = 0; i < trainers.size(); i++) {
                if (trainers.get(i).getId()== trainer_id) {
                    position = i;
                    break;
                }
            }

            ArrayList<Dates> d = new ArrayList<Dates>();
            d = trainers.get(position).getDates();
            for (int j = 0; j < d.size(); j++) {
                if (d.get(j).day.equals(day) && d.get(j).time == time) {
                   flage = true; 
                }
            }
            //End check if trainer has class in chossen day and time.
            
            //If trainer has class it choosen day and time show message to user else add class to data.
            if (flage) {
                JOptionPane.showMessageDialog(this, "Sory this trainer has another class in this day and time \n Choose Another Trainer or Change day or time");
            }
            else{ 
                
            //add day and time to trainer data.
            trainers.get(position).dates.add(new Dates(day,time));
            
            //Add new class to data.
            classes.add(new Class(type, discription, trainer_id, class_id, day, time, 0)); 
            
            //Show message to user to tell him that new class added.
            JOptionPane.showMessageDialog(this, ">> Class opned <<");
            
            //Add id of clss to edit_class_id_ComboBox to be able update data if want.
            edit_class_id_ComboBox.addItem(open_class_id_TextField.getText().toString());
            
            //Start Clear TextFieles after add member.
            open_class_id_TextField.setText(null);
            open_class_des_TextArea.setText(null);
            //End Clear TextFieles after add member.
            }
        }
    }//GEN-LAST:event_open_class_open_ButtonActionPerformed

